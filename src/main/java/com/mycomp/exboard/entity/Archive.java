package com.mycomp.exboard.entity;

public class Archive {

	private String name;
	private int book_id;
	
	public Archive() {
		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getBook_id() {
		return book_id;
	}

	public void setBook_id(int book_id) {
		this.book_id = book_id;
	}
	
}
